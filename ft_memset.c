#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	n;
	char	*ch;

	ch = (char *)b;
	n = 0;
	while (n < len)
	{
		ch[n] = c;
		n++;
	}
	return (b);
}
