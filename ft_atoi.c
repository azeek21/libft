#include "libft.h"

int	ft_atoi(const char *s)
{
	long long	r;
	int			i;
	int			m;

	i = 0;
	r = 0;
	m = 1;
	while ((s[i] == ' ') || (s[i] == '\n') || (s[i] == '\t')
		|| (s[i] == '\v') || (s[i] == '\f') || (s[i] == '\r'))
		i++;
	if ((s[i] == '-') || (s[i] == '+'))
	{
		if (s[i] == '-')
			m = m * -1;
		i++;
	}
	while ((s[i] >= '0') && (s[i] <= '9'))
	{
		if (r * 10 + (s[i] - '0') < 0)
			return (0);
		r = r * 10 + (s[i] - '0');
		i++;
	}
	return (m * r);
}
