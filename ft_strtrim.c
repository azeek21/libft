#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*s;
	int		end;

	if (!s1 || !set)
		return ((void *)0);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	end = ft_strlen(s1);
	while (end && ft_strchr(set, s1[end]))
		end--;
	s = ft_substr((char *)s1, 0, end + 1);
	return (s);
}
